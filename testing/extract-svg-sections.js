function extractSectionsFromSVG(svgContent) {
  const parser = new DOMParser();
  const doc = parser.parseFromString(svgContent, "text/xml");
  const sections = [];

  // Get all rect and path elements that represent seating sections
  const rects = doc.getElementsByTagName("rect");
  const paths = doc.getElementsByTagName("path");
  const texts = doc.getElementsByTagName("text");

  // Create a map of text elements by their position for matching with shapes
  const textMap = new Map();
  for (let i = 0; i < texts.length; i++) {
    const text = texts[i];
    const x = Number.parseFloat(text.getAttribute("x") || "0");
    const y = Number.parseFloat(text.getAttribute("y") || "0");
    const content = text.textContent?.trim();

    if (
      content &&
      (content.startsWith("U") ||
        content.startsWith("P") ||
        content.startsWith("L"))
    ) {
      textMap.set(`${Math.round(x)},${Math.round(y)}`, {
        content,
        x,
        y,
        color: text.getAttribute("fill") || "#ffffff",
        fontSize: Number.parseFloat(text.getAttribute("font-size") || "16"),
      });
    }
  }

  // Process rectangles
  for (let i = 0; i < rects.length; i++) {
    const rect = rects[i];
    const fill = rect.getAttribute("fill");

    // Skip if it's not a seating section (based on fill color)
    if (
      !fill ||
      fill === "#b3b3b3" ||
      fill === "#008000" ||
      fill === "#ffffff"
    ) {
      continue;
    }

    const x = Number.parseFloat(rect.getAttribute("x") || "0");
    const y = Number.parseFloat(rect.getAttribute("y") || "0");
    const width = Number.parseFloat(rect.getAttribute("width") || "0");
    const height = Number.parseFloat(rect.getAttribute("height") || "0");
    const transform = rect.getAttribute("transform");

    // Calculate center point for text matching
    let centerX = x + width / 2;
    let centerY = y + height / 2;

    // Apply transform if present (simplified - you may need more complex transform parsing)
    if (transform && transform.includes("matrix")) {
      // This is a simplified transform handling - you might need more robust parsing
      const matrixMatch = transform.match(/matrix$$([^)]+)$$/);
      if (matrixMatch) {
        const values = matrixMatch[1]
          .split(",")
          .map((v) => Number.parseFloat(v.trim()));
        if (values.length === 6) {
          const [a, b, c, d, e, f] = values;
          const newX = a * centerX + c * centerY + e;
          const newY = b * centerX + d * centerY + f;
          centerX = newX;
          centerY = newY;
        }
      }
    }

    // Find matching text
    let matchingText = null;
    let minDistance = Number.POSITIVE_INFINITY;

    for (const [key, textInfo] of textMap.entries()) {
      const distance = Math.sqrt(
        Math.pow(centerX - textInfo.x, 2) + Math.pow(centerY - textInfo.y, 2)
      );
      if (distance < minDistance && distance < 50) {
        // Within 50 units
        minDistance = distance;
        matchingText = textInfo;
      }
    }

    if (matchingText) {
      const section = {
        type: "rect",
        fill: fill,
        x: x,
        y: y,
        width: width,
        height: height,
        textX: centerX,
        textY: centerY,
        textColor: matchingText.color,
        fontSize: matchingText.fontSize,
        id: rect.getAttribute("id") || `rect_${i}`,
        name: matchingText.content,
        shape_class: "block",
        g_parent_class: determineParentClass(matchingText.content),
        g_parent_data_id: determineSectionId(matchingText.content),
      };

      if (transform) {
        section.transform = transform;
      }

      sections.push(section);
    }
  }

  // Process paths
  for (let i = 0; i < paths.length; i++) {
    const path = paths[i];
    const fill = path.getAttribute("fill");
    const d = path.getAttribute("d");

    // Skip if it's not a seating section
    if (
      !fill ||
      fill === "#b3b3b3" ||
      fill === "#008000" ||
      fill === "#ffffff" ||
      !d
    ) {
      continue;
    }

    // Calculate approximate center of path (simplified)
    const bbox = calculatePathBoundingBox(d);
    const centerX = bbox.x + bbox.width / 2;
    const centerY = bbox.y + bbox.height / 2;

    // Find matching text
    let matchingText = null;
    let minDistance = Number.POSITIVE_INFINITY;

    for (const [key, textInfo] of textMap.entries()) {
      const distance = Math.sqrt(
        Math.pow(centerX - textInfo.x, 2) + Math.pow(centerY - textInfo.y, 2)
      );
      if (distance < minDistance && distance < 50) {
        minDistance = distance;
        matchingText = textInfo;
      }
    }

    if (matchingText) {
      const section = {
        type: "path",
        fill: fill,
        d: d,
        textX: centerX,
        textY: centerY,
        textColor: matchingText.color,
        fontSize: matchingText.fontSize,
        id: path.getAttribute("id") || `path_${i}`,
        name: matchingText.content,
        shape_class: "block",
        g_parent_class: determineParentClass(matchingText.content),
        g_parent_data_id: determineSectionId(matchingText.content),
      };

      sections.push(section);
    }
  }

  return sections;
}

function calculatePathBoundingBox(d) {
  // Simplified bounding box calculation for paths
  // This is a basic implementation - you might need more sophisticated parsing
  const numbers = d.match(/-?\d+\.?\d*/g);
  if (!numbers || numbers.length < 2) {
    return { x: 0, y: 0, width: 0, height: 0 };
  }

  const coords = numbers.map((n) => Number.parseFloat(n));
  const minX = Math.min(...coords.filter((_, i) => i % 2 === 0));
  const maxX = Math.max(...coords.filter((_, i) => i % 2 === 0));
  const minY = Math.min(...coords.filter((_, i) => i % 2 === 1));
  const maxY = Math.max(...coords.filter((_, i) => i % 2 === 1));

  return {
    x: minX,
    y: minY,
    width: maxX - minX,
    height: maxY - minY,
  };
}

function determineParentClass(sectionName) {
  if (sectionName.startsWith("U")) {
    return "section tickets";
  } else if (sectionName.startsWith("P")) {
    return "section";
  } else if (sectionName.startsWith("L")) {
    return "section";
  }
  return "section";
}

function determineSectionId(sectionName) {
  const number = sectionName.match(/\d+/);
  if (number) {
    const num = Number.parseInt(number[0]);
    return `S_${num}`;
  }
  return "S_1";
}

