SCALE_FACTOR = 3

# Keys that should be scaled (if they exist in the shape)
coordinate_keys = [
    "x", "y", "width", "height", "textX", "textY", "fontSize"
]

def scale_path_d(d: str, factor: float) -> str:
    import re
    def repl(match):
        try:
            return str(float(match.group(0)) * factor)
        except ValueError:
            return match.group(0)

    return re.sub(r"-?\d+\.?\d*", repl, d)

def scale_shapes(shapes):
    scaled = []
    for shape in shapes:
        new_shape = deepcopy(shape)

        for key in coordinate_keys:
            if key in new_shape:
                new_shape[key] = round(float(new_shape[key]) * SCALE_FACTOR, 3)

        if new_shape.get("type") == "path" and "d" in new_shape and new_shape["d"]:
            new_shape["d"] = scale_path_d(new_shape["d"], SCALE_FACTOR)

        scaled.append(new_shape)
    return scaled

